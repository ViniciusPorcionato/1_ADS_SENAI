package aula1tb.examplevariables;

/**
 *
 * @author vestibular
 */
public class ExampleVariables {

    public static void main(String[] args) {
        int x = 2;
        float y = 2.54f;
        double z = 2.16;
        char a = 'A';
        String nome = "seu nome";
        boolean c = true;
        
        System.out.println("int x = " + x);
        System.out.println("float y = " + y);
        System.out.println("double z = " + z);
        System.out.println("char a = " + a);
        System.out.println("String nome = " + nome);
        System.out.println("boolean c = " + c);
    }
}
