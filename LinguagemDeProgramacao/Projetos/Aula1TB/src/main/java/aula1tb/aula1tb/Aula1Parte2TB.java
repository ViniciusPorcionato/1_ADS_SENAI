public class Aula1Parte2TB {

    public static void main(String[] args) {
        int A = 1;
        int B = 2;
        if (A == B) {
            System.out.println("São iguais");
        } else if (A > B) {
            System.out.println("A é maior que B");
        } else if (B > A) {
            System.out.println("B é maior que A");
        } else {
            System.out.println("Números inválidos");
        }
    }
}
